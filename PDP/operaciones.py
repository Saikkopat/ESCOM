def suma(num1, num2):
        res = num1 + num2
        return res

def resta(num1, num2):
        res = num1 - num2
        return res

def mult(num1, num2):
        res = num1 * num2
        return res

def div(num1, num2):
        res = num1 / num2
        return res

def mod(num1, num2):
        res = num1 % num2
        return res